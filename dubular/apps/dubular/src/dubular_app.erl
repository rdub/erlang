-module(dubular_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    dubular_sup:start_link().
    %% Get shit up to date right off the bat...
    %ota:perform_all_updates(),
    %{ok, self()}.

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

